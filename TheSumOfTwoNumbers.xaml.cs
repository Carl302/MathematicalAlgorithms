    /// <summary>
    /// 两数之和 的交互逻辑
    /// </summary>
    public partial class TheSumOfTwoNumbers : Window
    {
        public TheSumOfTwoNumbers()
        {
            InitializeComponent();
        }
        public int[] TwoSum(int[] nums, int target)
        {
            for (int i = 0; i < nums.Length; i++)
            {
                for (int j = i + 1; j < nums.Length; j++)
                {
                    if (nums[i] + nums[j] == target)
                    {
                        return new int[] { i, j };
                    }
                }
            }
            return new int[0];
        }
    }
